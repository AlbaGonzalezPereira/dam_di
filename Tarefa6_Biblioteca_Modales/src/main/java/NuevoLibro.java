    private void botonAnhadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAnhadirActionPerformed
        //Creamos las variables que vamos a usar:
        ButtonModel seleccion = buttonGroup1.getSelection();
        int valor = (int) valoracion.getValue();
        Libro libro;
        String tipo;
        JDialog dialogoModal = new JDialog(this, "Error de validación", true);
        dialogoModal.setLayout(new FlowLayout());
        dialogoModal.setSize(200, 100);
        

        //cuando se da al botón añadir libro:
        if (titulo.getText().equals("")) {
            JLabel titulo = new JLabel("Debe introducir un título.");
            JButton aceptar = new JButton("Aceptar");
            aceptar.addActionListener(new ActionListener(){
                @Override
                public void actionPerformed(ActionEvent e) {
                    dialogoModal.dispose();//cerramos ventana
                }
            });
            dialogoModal.add(titulo);
            dialogoModal.add(aceptar);
            dialogoModal.setLocationRelativeTo(this);
            dialogoModal.setVisible(true);
            return;
        }

        // Si los autores está en el index 0 es que no se ha escogido ningún autor
        if (autores.getSelectedIndex() == 0) {
            JLabel autor = new JLabel("Debe escoger un autor.");
            JButton aceptar = new JButton("Aceptar");
            aceptar.addActionListener(new ActionListener(){
                @Override
                public void actionPerformed(ActionEvent e) {
                    dialogoModal.dispose();//cerramos ventana
                }
            });
            dialogoModal.add(autor);
            dialogoModal.add(aceptar);
            dialogoModal.setLocationRelativeTo(this);
            dialogoModal.setVisible(true);
            return;
        }

        //Si la editorial está en el index 0 es que no se ha escogido ninguna
        if (editorial.getSelectedIndex() == 0) {
            JLabel editorial = new JLabel("Debe escoger una editorial.");
            JButton aceptar = new JButton("Aceptar");
            aceptar.addActionListener(new ActionListener(){
                @Override
                public void actionPerformed(ActionEvent e) {
                    dialogoModal.dispose();//cerramos ventana
                }
            });
            dialogoModal.add(editorial);
            dialogoModal.add(aceptar);
            dialogoModal.setLocationRelativeTo(this);
            dialogoModal.setVisible(true);
            return;
        }

        // Comprobamos que se ha marcado algún radiobutton
        if (seleccion != novela.getModel() && seleccion != biblio.getModel() && seleccion != poesia.getModel()) {
            JLabel tipoLibro = new JLabel("Debe escoger un tipo de libro.");
            JButton aceptar = new JButton("Aceptar");
            aceptar.addActionListener(new ActionListener(){
                @Override
                public void actionPerformed(ActionEvent e) {
                    dialogoModal.dispose();//cerramos ventana
                }
            });
            dialogoModal.add(tipoLibro);
            dialogoModal.add(aceptar);
            dialogoModal.setLocationRelativeTo(this);
            dialogoModal.setVisible(true);
            return;
        }

        //comprobamos que el valor se ha escogido
        if (valor < 0 || valor > 5) {
            JLabel tipoLibro = new JLabel("Debe escoger un tipo de libro.");
            JButton aceptar = new JButton("Aceptar");
            aceptar.addActionListener(new ActionListener(){
                @Override
                public void actionPerformed(ActionEvent e) {
                    dialogoModal.dispose();//cerramos ventana
                }
            });
            dialogoModal.add(tipoLibro);
            dialogoModal.add(aceptar);
            dialogoModal.setLocationRelativeTo(this);
            dialogoModal.setVisible(true);
            return;
        }

        //Comprobamos qué tipo está seleccionado
        if (novela.isSelected()) {
            tipo = novela.getText();
        } else if (biblio.isSelected()) {
            tipo = biblio.getText();
        } else {
            tipo = poesia.getText();
        }

        //creamos el libro y lo añadimos al ArrayList
        libro = new Libro(titulo.getText(), (String) autores.getSelectedItem(), (String) editorial.getSelectedItem(), tipo, valor);
        libros.add(libro); // añadimos el arraylist

        //Comprobamos que sale el mensaje
        System.out.println("Libro añadido correctamente");
    }//GEN-LAST:event_botonAnhadirActionPerformed

    // método que muestra los libros de la biblioteca
