    // Método que añade un autor con un diálogo no modal
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        //creamos el diálogo no modal:
        JDialog dialogo = new JDialog(this, "Insertar autor");
        dialogo.setLayout(new FlowLayout());
        
        //creamos los elementos del diálogo
        JLabel nombreAutor = new JLabel("Introduce el nombre del autor:");
        JTextField autor = new JTextField(30);
        JButton aceptar = new JButton("Aceptar");
        JButton cancelar = new JButton("Cancelar");
        JFrame frame = this;
        
        //creamos los eventos de aceptar y cancelar
        cancelar.addActionListener(new ActionListener(){
            @Override
            public void actionPerformed(ActionEvent e) {
                dialogo.dispose();
            }
        });
        
        aceptar.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                JDialog dialogoAcept = new JDialog(frame, "Aceptar", true); //diálogo modal
                dialogoAcept.setLayout(new FlowLayout());
                JLabel confirm = new JLabel("¿Quiere añadir el autor a la lista?");
                JButton yes = new JButton("Sí");
                JButton no = new JButton("No");
                
                //creamos los eventos de los botones:
                yes.addActionListener(new ActionListener(){
                    @Override
                    public void actionPerformed(ActionEvent e) {
                        autores.addItem(autor.getText());//añadimos el autor a la lista de autores
                        autor.setText(""); //ponemos el texto en blanco
                        dialogoAcept.dispose();//cerramos el diálogo modal
                    }  
                });
                        
                no.addActionListener(new ActionListener(){
                    @Override
                    public void actionPerformed(ActionEvent e) {
                        dialogoAcept.dispose();
                    }
                });
                
                //añadimos los elementos al JDialog
                dialogoAcept.add(confirm);
                dialogoAcept.add(yes);
                dialogoAcept.add(no);
                
                //ponemos las medidas:
                dialogoAcept.setSize(200,100);
                dialogoAcept.setLocationRelativeTo(frame);
                dialogoAcept.setVisible(true);
               
            }
        });
        
        //agregamos los elementos
        dialogo.add(nombreAutor);
        dialogo.add(autor);
        dialogo.add(aceptar);
        dialogo.add(cancelar);
        
        //ponemos un tamaño, lugar
        dialogo.setSize(400, 150);
        dialogo.setLocationRelativeTo(this);
        
        //lo hacemos visible
        dialogo.setVisible(true);
       
    }//GEN-LAST:event_jButton3ActionPerformed

