    private void jbConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbConfirmarActionPerformed
        String origen = jcbOrigen.getSelectedItem().toString();
        String destino = jcbDestino.getSelectedItem().toString();
        String usuario = jtxNombre.getText() + " " + jtxApellidos.getText();
        String fecha = jcbDia.getSelectedItem().toString() + " de " + jcbMes.getSelectedItem().toString() + " de " + jcbAno.getSelectedItem().toString();
        System.out.println(fecha);

        int opcion = JOptionPane.showConfirmDialog(null, "Los datos del vuelo son: Origen->" + origen + " - Destino->" + destino + "\n"
                + "Los datos del usuario son: " + usuario + "\n"
                + "Los datos de la reserva: " + fecha, "Confirmación de la reserva", JOptionPane.YES_NO_OPTION);
        if (opcion == JOptionPane.YES_OPTION) {
            JOptionPane.showMessageDialog(null, "Datos guardados", "Datos de la reserva", JOptionPane.INFORMATION_MESSAGE);
        }
        if (opcion == JOptionPane.NO_OPTION) {
            JOptionPane.showMessageDialog(null, "Datos no guardados", "Datos de la reserva", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jbConfirmarActionPerformed

