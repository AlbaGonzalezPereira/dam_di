    private void jmiMostrarVActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jmiMostrarVActionPerformed
        JDialog dialogoMostrar = new JDialog(this, "Vuelos");
        dialogoMostrar.setLayout(new FlowLayout());
        dialogoMostrar.setSize(400, 100);
        ArrayList<String> vuelos = new ArrayList<>();
        vuelos.add("Vigo-Madrid");
        vuelos.add("Barcelona-Vigo");
        vuelos.add("Madrid-Barcelona");
        vuelos.add("Vigo-Málaga");
        JButton cerrar = new JButton("Cerrar ventana");
        cerrar.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                dialogoMostrar.dispose();//cerramos ventana
            }
        });
        for (String vuelo : vuelos) {
            dialogoMostrar.add(new JLabel(vuelo + ","));     
        }        
        dialogoMostrar.add(cerrar);
        dialogoMostrar.setVisible(true);
    }//GEN-LAST:event_jmiMostrarVActionPerformed

